class BooleanDataType  {
   public static void main(String[] args) {
        boolean isTall = true;
        boolean isShort = false;
        
        System.out.println("Is it Tall? " + isTall);
        System.out.println("Is it Short? " + isShort);
        
        
        boolean result = (2 > 9); 
        boolean isEqual = (10 == 15); 
      
        
        System.out.println("2 > 9? " + result);
        System.out.println("10 == 15? " + isEqual);
        
        
       
    }
}